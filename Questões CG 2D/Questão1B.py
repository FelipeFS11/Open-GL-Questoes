import OpenGL.GL as gl
import OpenGL.GLUT as glut

def display():
    glut.glutSwapBuffers()
    gl.glClear(gl.GL_COLOR_BUFFER_BIT)
    gl.glClearColor(1.0, 1.0, 1.0, 1.0)

    gl.glShadeModel(gl.GL_FLAT)
    gl.glBegin(gl.GL_TRIANGLES)
    gl.glColor3f(1, 0.08, 0.58)
    gl.glVertex3f(-0.7, 0.2, 0.0)
    gl.glVertex3f(-0.5, 0.6, 0.0)
    gl.glVertex3f(-0.3, 0.2, 0.0)
    gl.glEnd()
    gl.glFlush()

    gl.glShadeModel(gl.GL_FLAT)
    gl.glBegin(gl.GL_TRIANGLES)
    gl.glColor3f(0.24, 0.7, 0.44)
    gl.glVertex3f(0.2, 0.2, 0.0)
    gl.glVertex3f(0.5, 0.2, 0.0)
    gl.glVertex3f(0.8, 0.5, 0.0)
    gl.glEnd()
    gl.glFlush()

    gl.glShadeModel(gl.GL_FLAT)
    gl.glBegin(gl.GL_QUADS)
    gl.glColor3f(0.39, 0.58, 0.93)
    gl.glVertex3f(-0.2, -0.2, 0.0)
    gl.glVertex3f(-0.7, -0.2, 0.0)
    gl.glVertex3f(-0.7, -0.7, 0.0)
    gl.glVertex3f(-0.2, -0.7, 0.0)
    gl.glEnd()
    gl.glFlush()

    gl.glShadeModel(gl.GL_FLAT)
    gl.glBegin(gl.GL_QUADS)
    gl.glColor3f(0.7, 0.13, 0.13)
    gl.glVertex3f(0.2, -0.2, 0.0)
    gl.glVertex3f(0.7, -0.2, 0.0)
    gl.glVertex3f(0.7, -0.7, 0.0)
    gl.glVertex3f(0.2, -0.7, 0.0)
    gl.glEnd()
    gl.glFlush()

glut.glutInit()
glut.glutInitDisplayMode(0)
glut.glutCreateWindow(b'Felipe Freire')
glut.glutReshapeWindow(512, 512)
glut.glutDisplayFunc(display)
glut.glutMainLoop()